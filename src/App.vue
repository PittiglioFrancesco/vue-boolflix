<template>
  <div id="app">
    <Header @ricerca="ricercaFilm"/>
    <Main :films="arrayFilm" :campoRicerca="searchText" />
  </div>
</template>

<script>
import Header from './components/Header.vue'
import Main from './components/Main.vue'
import axios from 'axios'


export default {
  name: 'App',
  components: {
    Header,
    Main
  },
  data(){
    return{
      apiURLMovie:'https://api.themoviedb.org/3/search/movie?',
      apiURLSerie:'https://api.themoviedb.org/3/search/tv?',
      apiKey:'ee7c70302b256bf9639aba651036d09e',
      language: 'it_IT',
      arrayFilm: [],
      arraySerie: [],
      searchText: ''
    }
  },
  methods:{
    ricercaFilm(testo){
      this.searchText = testo;
      axios
      .get(this.apiURLMovie, {
        params: {
          api_key: this.apiKey,
          language: this.language,
          query: testo
        }
      })
      .then(response =>  {
        console.log(response.data.results)
        this.arrayFilm = response.data.results;
      });
      console.log(testo);
    },
    ricercaSerie(testo){
      this.searchText = testo;
      axios
      .get(this.apiURLSerie, {
        params: {
          api_key: this.apiKey,
          language: this.language,
          query: testo
        }
      })
      .then(risposta =>  {
        console.log(risposta.data.results)
        this.arraySerie = risposta.data.results;
      });
      console.log(testo);
    }
  }
}
</script>

